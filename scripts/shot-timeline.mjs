import { chromium } from 'playwright-core'
const browser = await chromium.launch({ executablePath: '/usr/bin/chromium-browser' }).catch(() => chromium.launch())
const ctx = await browser.newContext({ viewport: { width: 1600, height: 1200 } })
const page = await ctx.newPage()
const errors = []
page.on('pageerror', (e) => errors.push(`pageerror: ${e.message}`))
page.on('console', (msg) => { if (msg.type() === 'error' && !msg.text().includes('401')) errors.push(`console.error: ${msg.text()}`) })
await page.goto('http://localhost:5173/login', { waitUntil: 'networkidle' })
await page.fill('input[autocomplete="username"]', 'admin')
await page.fill('input[autocomplete="current-password"]', 'admin123456')
await page.click('button[type="submit"]')
await page.waitForURL(/\/board/, { timeout: 5000 })
await page.goto('http://localhost:5173/board/expansion/view/timeline', { waitUntil: 'networkidle' })
await page.waitForTimeout(1000)
await page.screenshot({ path: '/tmp/timeline-8step.png', fullPage: true })
console.log('errors:', errors.length)
console.log('milestone cards:', await page.locator('.milestone-card').count())
console.log('milestone dots:', await page.locator('.milestone-dot').count())
console.log('axis labels:', await page.locator('.gantt-axis-label').count())
await browser.close()
